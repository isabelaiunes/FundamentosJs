var quantidade = 10;
// var é variável e igual (=) é símbolo de atribuição. "10" está armazenado na var quantidade.
// Atualmente, usa-se let em vez de var e há algumas diferenças. Var permite redeclaração, let não.
// O nome da variável é o identificador.
var nome = "Caneta"
let preco = 6.4;
let imposto = 1.5;
var precoFinal = preco + imposto

console.log("Caneta");
console.log(nome);
console.log(10); // valor literal = direto
console.log(quantidade); // Mostrar var ao invés do valor literal.
console.log(preco);
console.log(imposto);
console.log(7.9);
console.log(preco + imposto);
console.log(precoFinal);

// Casas decimais em JavaScript são separadas por ponto (.), e não vírgula (,).
// Letras maiúsculas e minúsculas importam em JavaScript.

